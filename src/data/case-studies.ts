export function getCaseStudyById(id: string): CaseStudy | undefined {
  return caseStudies.find((cs) => cs.id === id);
}

export function getCaseStudiesByIndustry(industry: Industry): CaseStudy[] {
  return caseStudies.filter((cs) => cs.industry === industry);
}

export function getCaseStudiesByTechnology(tech: string): CaseStudy[] {
  return caseStudies.filter((cs) => cs.technology.includes(tech));
}

export function getAllIndustries(): Industry[] {
  return [...new Set(caseStudies.map((cs) => cs.industry))];
}

export function getAllTechnologies(): string[] {
  const techCount = new Map<string, number>();
  for (const cs of caseStudies) {
    for (const t of cs.technology) {
      techCount.set(t, (techCount.get(t) || 0) + 1);
    }
  }
  return [...techCount.entries()]
    .sort((a, b) => b[1] - a[1])
    .map(([t]) => t);
}
